    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        if (alquilerSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un alquiler de la lista superior.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(this,
                "¿Confirmar devolución del Alquiler ID: " + alquilerSeleccionado.getIdAlquiler() + "?\n"
                + "Los productos volverán al stock.",
                "Procesar Devolución",
                JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {
            // Lógica central de devolución
            boolean exito = sistema.procesarDevolucion(alquilerSeleccionado);

            if (exito) {
                JOptionPane.showMessageDialog(this, "Devolución exitosa. Stock actualizado.");

                // Refrescar todo
                cargarListaAlquileres();
                limpiarDetalles();

            } else {
                JOptionPane.showMessageDialog(this, "Error al procesar la devolución.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDevolverActionPerformed

