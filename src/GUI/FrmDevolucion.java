    private void limpiarDetalles() {
        tblDetalle.setModel(new DefaultTableModel(new String[]{"Producto", "Categoría", "Precio Unit."}, 0));
        lblInfo.setText("Seleccione un alquiler para ver el detalle.");
        btnDevolver.setEnabled(false);
        alquilerSeleccionado = null;
    }

    private void cargarDetalleAlquiler(Alquiler alquiler) {
        if (alquiler == null) {
            return;
        }

        DefaultTableModel modelo = new DefaultTableModel(new String[]{"Producto", "Categoría", "Precio Unit."}, 0);

        for (Producto p : alquiler.getProductos()) {
            modelo.addRow(new Object[]{
                p.getNomProducto(),
                p.getCatProducto(),
                String.format("S/ %.2f", p.getPrecio())
            });
        }
        tblDetalle.setModel(modelo);

        lblInfo.setText("Alquiler ID: " + alquiler.getIdAlquiler() + " - Cliente: " + alquiler.getCliente().getNombre());
        btnDevolver.setEnabled(true);
    }

